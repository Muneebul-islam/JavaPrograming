public class Boolean_logical_operators {
    public static void main(String[] args) {
        // This Java program demonstrates the use of bitwise and logical operators on integer variables
        // `a` and `b`.
        int a = 5;
        int b = 4;
        System.out.println(~a);
        System.out.println(a|b);
        System.out.println(a&b);
        System.out.println(a<<2);
        System.out.println(b>>3);
        System.out.println(a^b);
    }
}
